#include <stdio.h>
#include <string.h>

int main() {
    char S[101];
    scanf("%s", S);

    int pos[26]; // 알파벳 위치를 저장할 배열, 초기값은 0
    // 모든 알파벳 위치를 -1로 초기화
    for (int i = 0; i < 26; i++) {
        pos[i] = -1;
    }

    // 문자열을 한 번 순회하며 첫 등장 위치만 기록
    int len = strlen(S);
    for (int i = 0; i < len; i++) {
        int idx = S[i] - 'a';
        if (pos[idx] == -1) {
            pos[idx] = i;
        }
    }

    // a부터 z까지 차례로 출력
    for (int i = 0; i < 26; i++) {
        printf("%d", pos[i]);
        if (i < 25) printf(" ");
    }
    printf("\n");
    return 0;
}