#include <stdio.h>

int main() {
    int N;
    if (scanf("%d", &N) != 1) return 0;

    int bags = 0;
    while (N >= 0) {
        if (N % 5 == 0) {
            bags += N / 5;
            printf("%d\n", bags);
            return 0;
        }
        N -= 3;
        bags++;
    }
    printf("-1\n");
    return 0;
}