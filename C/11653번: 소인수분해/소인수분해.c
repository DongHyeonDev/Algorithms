#include <stdio.h>

int main() {
    int n;
    if (scanf("%d", &n) != 1) return 0;
    if (n == 1) return 0;

    while (n % 2 == 0) {
        printf("2\n");
        n /= 2;
    }

    for (int i = 3; i * i <= n; i += 2) {
        while (n % i == 0) {
            printf("%d\n", i);
            n /= i;
        }
    }

    if (n > 1) {
        printf("%d\n", n);
    }
    return 0;
}