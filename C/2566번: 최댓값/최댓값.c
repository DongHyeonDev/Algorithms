#include <stdio.h>

int main() {
    int A[9][9];
    int max = 0, x = 0, y = 0;

    for (int i = 0; i < 9; i++) {
        for (int j = 0; j < 9; j++) {
            scanf("%d", &A[i][j]);
            if (A[i][j] > max) {
                max = A[i][j];
                x = i + 1;
                y = j + 1;
            }
        }
    }

    printf("%d\n%d %d\n", max, x, y);
    return 0;
}