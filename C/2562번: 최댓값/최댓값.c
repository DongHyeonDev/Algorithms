#include <stdio.h>

int main() {
    int max = -1, idx = 0;
    
    for (int i = 1; i <= 9; i++) {
        int x;
        scanf("%d", &x);
        if (x > max) {
            max = x;
            idx = i;
        }
    }
    printf("%d\n%d\n", max, idx);
    
    return 0;
}