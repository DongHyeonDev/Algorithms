#include <stdio.h>

int main() {
    int N;
    int p = 1;
    if (scanf("%d", &N) != 1) return 0;

    while ((p << 1) <= N) p <<= 1;
    if (N == p) printf("%d\n", N);
    else printf("%d\n", 2 * (N - p));
    return 0;
}