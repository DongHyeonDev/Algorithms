#include <stdio.h>

int main() {
    int submitted[31] = {0};

    // 28명의 제출 번호를 입력받아 마킹
    for (int i = 0; i < 28; i++) {
        int n;
        scanf("%d", &n);
        submitted[n] = 1;
    }

    // 제출하지 않은 번호 두 개를 찾아 출력
    for (int i = 1; i <= 30; i++) {
        if (!submitted[i]) {
            printf("%d\n", i);
        }
    }

    return 0;
}