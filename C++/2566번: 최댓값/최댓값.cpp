#include <iostream>

int main() {
    int arr[9][9];
    int max = 0, x = 0, y = 0;

    for (int i = 0; i < 9; ++i) {
        for (int j = 0; j < 9; ++j) {
            std::cin >> arr[i][j];
            if (arr[i][j] > max) {
                max = arr[i][j];
                x = i + 1;
                y = j + 1;
            }
        }
    }

    std::cout << max << "\n" << x << " " << y << "\n";
    return 0;
}