#include <iostream>

int main() {
    int n;
    if (!(std::cin >> n)) return 0;
    if (n == 1) return 0;

    while (n % 2 == 0) {
        std::cout << 2 << '\n';
        n /= 2;
    }

    for (int i = 3; 1LL * i * i <= n; i += 2) {
        while (n % i == 0) {
            std::cout << i << '\n';
            n /= i;
        }
    }

    if (n > 1) {
        std::cout << n << '\n';
    }
    return 0;
}