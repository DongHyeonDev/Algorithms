#include <iostream>

int main() {
    int N; 
    if (!(std::cin >> N)) return 0;

    int bags = 0;
    while (N >= 0) {
        if (N % 5 == 0) {
            std::cout << bags + (N / 5) << '\n';
            return 0;
        }
        N -= 3;
        ++bags;
    }
    std::cout << -1 << '\n';
    return 0;
}