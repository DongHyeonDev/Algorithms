#include <iostream>

int main() {
    int seen[42] = {0};
    int x;

    // 10개의 수를 입력받아 42로 나눈 나머지를 마킹
    for (int i = 0; i < 10; i++) {
        std::cin >> x;
        seen[x % 42] = 1;
    }

    // 서로 다른 나머지 개수 세기
    int count = 0;
    for (int i = 0; i < 42; i++) {
        if (seen[i]) {
            count++;
        }
    }

    // 결과 출력
    std::cout << count << "\n";
    return 0;
}
