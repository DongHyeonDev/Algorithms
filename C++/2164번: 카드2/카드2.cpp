#include <iostream>

int main() {
    int N;
    int p = 1;
    if (!(std::cin >> N)) return 0;

    while ((p << 1) <= N) p <<= 1;
    std::cout << (N == p ? N : 2 * (N - p)) << '\n';
    return 0;
}