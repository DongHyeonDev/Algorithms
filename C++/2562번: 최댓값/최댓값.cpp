#include <iostream>

int main() {
    int max = -1, idx = 0;

    for (int i = 1; i <= 9; ++i) {
        int x;
        std::cin >> x;
        if (x > max) {
            max = x;
            idx = i;
        }
    }
    std::cout << max << "\n" << idx << "\n";
    
    return 0;
}